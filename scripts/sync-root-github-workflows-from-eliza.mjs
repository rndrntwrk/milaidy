#!/usr/bin/env node
/**
 * Generates workflows under `.github/workflows/` from `eliza/.github/workflows/`
 * so `workflow_call` targets in agent-release.yml resolve on milady-ai/milady.
 *
 * Layout differences:
 * - Library paths use `eliza/packages/*` (submodule) instead of monorepo `packages/*`.
 * - Shell app lives at `apps/app` instead of `packages/app`.
 * - Release URLs / dispatch repos use `milady-ai/*` instead of `elizaOS/*` where applicable.
 *
 * Run after bumping the eliza submodule when upstream workflow YAML changes:
 *   node scripts/sync-root-github-workflows-from-eliza.mjs
 */

import fs from "node:fs";
import path from "node:path";

const repoRoot = path.resolve(import.meta.dirname, "..");
const srcDir = path.join(repoRoot, "eliza", ".github", "workflows");
const dstDir = path.join(repoRoot, ".github", "workflows");

const SOURCES = [
  "release-electrobun.yml",
  "publish-packages.yml",
  "android-release.yml",
  "apple-store-release.yml",
  "update-homebrew.yml",
];

const MASK_APP_CORE = "\x00MASK_APP_CORE\x00";

/** @param {string} name */
function transform(name, content) {
  let out = content;

  // Prevent `packages/app` rules from corrupting `packages/app-core` (substring overlap).
  out = out.split("packages/app-core").join(MASK_APP_CORE);

  const pathPairs = [
    ["packages/shared", "eliza/packages/shared"],
    ["packages/schemas", "eliza/packages/schemas"],
    ["packages/typescript", "eliza/packages/typescript"],
    ["apps/browser-bridge", "eliza/apps/browser-bridge"],
    ["packages/app/", "apps/app/"],
    ["packages/app", "apps/app"],
  ];
  for (const [from, to] of pathPairs) {
    out = out.split(from).join(to);
  }

  out = out.split(MASK_APP_CORE).join("eliza/packages/app-core");

  // GitHub org / repository identifiers for Milady product releases
  const orgPairs = [
    ["elizaOS/eliza", "milady-ai/milady"],
    ["repos/elizaOS/apt", "repos/milady-ai/apt"],
  ];
  if (name === "update-homebrew.yml") {
    orgPairs.push(["elizaOS/homebrew-tap", "milady-ai/homebrew-tap"]);
  }
  for (const [from, to] of orgPairs) {
    out = out.split(from).join(to);
  }

  const banner = `# @generated by scripts/sync-root-github-workflows-from-eliza.mjs
# Source: eliza/.github/workflows/${name} — re-run sync after eliza submodule bumps.

`;

  return banner + out;
}

function patchReleaseOrchestrator() {
  const orchestratorPath = path.join(dstDir, "release-orchestrator.yml");
  let raw = fs.readFileSync(orchestratorPath, "utf8");
  if (raw.includes("publish-npm:") && raw.includes("uses: ./.github/workflows/publish-npm.yml")) {
    console.log("release-orchestrator.yml already contains Milady publish-npm / deploy-homepage jobs; skipping patch.");
    return;
  }

  const insertAfter = `    secrets: inherit

  summary:`;

  const injection = `    secrets: inherit

  publish-npm:
    name: Publish npm (miladyai)
    needs: prepare
    if: needs.prepare.outputs.publish_npm == 'true'
    uses: ./.github/workflows/publish-npm.yml
    with:
      version: \${{ needs.prepare.outputs.version }}
      dist_tag: \${{ needs.prepare.outputs.npm_dist_tag }}
    secrets: inherit

  deploy-homepage:
    name: Deploy homepage (GitHub Pages)
    needs: prepare
    if: needs.prepare.outputs.deploy_homepage == 'true'
    uses: ./.github/workflows/deploy-web.yml
    with:
      release_version: \${{ needs.prepare.outputs.tag }}
    secrets: inherit

  summary:`;

  if (!raw.includes(insertAfter)) {
    throw new Error(
      "release-orchestrator.yml: expected anchor for Milady job injection not found; sync eliza release-orchestrator.yml first.",
    );
  }
  raw = raw.replace(insertAfter, injection);

  raw = raw.replace(
    /needs:\s*\n\s*\[\s*\n\s*prepare,\s*\n\s*publish-packages,\s*\n\s*publish-android,\s*\n\s*publish-apple,\s*\n\s*update-homebrew,\s*\n\s*\]/m,
    `needs:
      [
        prepare,
        publish-packages,
        publish-android,
        publish-apple,
        update-homebrew,
        publish-npm,
        deploy-homepage,
      ]`,
  );

  raw = raw.replace(
    /echo "\| Homebrew \| \$\{\{ needs\.update-homebrew\.result \|\| 'skipped' \}\} \|"/,
    `echo "| Homebrew | \${{ needs.update-homebrew.result || 'skipped' }} |"
            echo "| npm (miladyai) | \${{ needs.publish-npm.result || 'skipped' }} |"
            echo "| Homepage (Pages) | \${{ needs.deploy-homepage.result || 'skipped' }} |"`,
  );

  raw = raw.replace(
    /require_result "Homebrew" "\$\{\{ needs\.prepare\.outputs\.update_homebrew \}\}" "\$\{\{ needs\.update-homebrew\.result \}\}"/,
    `require_result "Homebrew" "\${{ needs.prepare.outputs.update_homebrew }}" "\${{ needs.update-homebrew.result }}"
          require_result "npm publish" "\${{ needs.prepare.outputs.publish_npm }}" "\${{ needs.publish-npm.result }}"
          require_result "Homepage deploy" "\${{ needs.prepare.outputs.deploy_homepage }}" "\${{ needs.deploy-homepage.result }}"`,
  );

  fs.writeFileSync(orchestratorPath, raw);
  console.log("Patched release-orchestrator.yml with publish-npm + deploy-homepage jobs.");
}

function main() {
  fs.mkdirSync(dstDir, { recursive: true });

  for (const file of SOURCES) {
    const srcPath = path.join(srcDir, file);
    if (!fs.existsSync(srcPath)) {
      console.error(`Missing source workflow: ${srcPath}`);
      process.exit(1);
    }
    const transformed = transform(file, fs.readFileSync(srcPath, "utf8"));
    const dstPath = path.join(dstDir, file);
    fs.writeFileSync(dstPath, transformed);
    console.log(`Wrote ${path.relative(repoRoot, dstPath)}`);
  }

  const orchSrc = path.join(srcDir, "release-orchestrator.yml");
  if (!fs.existsSync(orchSrc)) {
    console.error(`Missing ${orchSrc}`);
    process.exit(1);
  }
  const orchOut = transform("release-orchestrator.yml", fs.readFileSync(orchSrc, "utf8"));
  fs.writeFileSync(path.join(dstDir, "release-orchestrator.yml"), orchOut);
  console.log(`Wrote ${path.relative(repoRoot, path.join(dstDir, "release-orchestrator.yml"))}`);

  patchReleaseOrchestrator();
}

main();
